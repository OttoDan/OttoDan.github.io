var canvas;
function setup() {
  canvas = createCanvas(windowWidth-windowWidth*0.005,windowHeight-windowWidth*0.005);
  canvas.position(0,0);
  canvas.style('z-index','-1');
  frameRate(30);

  //Logo
  logoPixelSize = windowWidth/logoScaleDivider;
  var i=0;
  logoCounter = 0;
  for(var iY=0; iY < logoHeight; iY++){
    for(var iX=0; iX < logoWidth; iX++){
      i++;
      if(logoStr.charAt(i) != "x"){
        logoPixelDestinationX.push(iX*logoPixelSize);
        logoPixelDestinationY.push(iY*logoPixelSize);
        logoPixelPosX.push(random(-width*2,width*2));
        logoPixelPosY.push(random(-height*2,height*2));
        logoPixelColor.push(logoStr.charAt(i));
        logoCounter ++;
      }
    }
  }
  noStroke();

  //application string
  applicationLetterSize = logoPixelSize*8;
  textSize(applicationLetterSize);
  applicationLetterDestinationY = -32;//windowHeight/2 + applicationLetterSize*0.5;
  var lastWidth=0;

  for(var j=0; j<applicationStr.length; j++)
  {
    applicationLetterPosX[j] = random(-windowWidth,windowWidth);
    applicationLetterPosY[j] = random(-windowHeight,windowHeight);
    applicationLetterDestinationX[j] = -128 + lastWidth;
    lastWidth+=textWidth(applicationStr[j]);
  }
}

function draw() {
  background(212);
  //textSize(64);
  //text("HEY", windowWidth*0.5, windowHeight*0.5);
//  textFont('Droid Sans');
  textStyle(NORMAL);
  //text(scrollPosY + " scale:" + logoScaleFactor,0,64);
  stroke("black");
  strokeWeight(4);
  line(0,-scrollPosY,windowWidth,-scrollPosY);
  textSize(applicationLetterSize/2);
    textStyle(NORMAL);
//  text(applicationCoverText,windowWidth/9,windowHeight-applicationCoverTextPosY+scrollPosY*8,windowWidth*0.75,windowHeight*0.75);



  //applicationStr
  noStroke();
  //translate(0,scrollPosY*logoPixelSize);
  //applicationLetterInPosCount=0;
  if(scrollPosY>=-3){

    logoScaleFactor = lerp(logoScaleFactor,1,0.1);
    scale(logoScaleFactor,logoScaleFactor);
    translate(windowWidth/2-logoWidth*logoPixelSize*logoScaleFactor*0.5, windowHeight/2-logoHeight*logoPixelSize*logoScaleFactor*0.5);
  } else {
    logoScaleFactor = lerp(logoScaleFactor,0.25,0.1);
    scale(logoScaleFactor,logoScaleFactor);
    translate(windowWidth/2-logoWidth*logoPixelSize*logoScaleFactor*0.5,windowHeight/2-logoHeight*logoPixelSize*logoScaleFactor*0.5);
  }
  textSize(applicationLetterSize);
  textStyle(BOLD);
  for(var j=0; j<applicationStr.length; j++)
  {
    if(abs(applicationLetterPosX[j]-applicationLetterDestinationX[j]) < 0.5 && abs(applicationLetterPosY[j]-applicationLetterDestinationY) < 0.5)
      applicationLetterInPosCount++;
    else{
      if(applicationLetterPosX[j] > applicationLetterDestinationX[j])
        applicationLetterPosX[j] -= abs(applicationLetterPosX[j]-applicationLetterDestinationX[j])*0.06 ;

      if(applicationLetterPosX[j] < applicationLetterDestinationX[j])
        applicationLetterPosX[j] += abs(applicationLetterPosX[j]-applicationLetterDestinationX[j])*0.06 ;

      if(applicationLetterPosY[j] > applicationLetterDestinationY)
        applicationLetterPosY[j] -= abs(applicationLetterPosY[j]-applicationLetterDestinationY)*0.06 ;

      if(applicationLetterPosY[j] < applicationLetterDestinationY)
        applicationLetterPosY[j] += abs(applicationLetterPosY[j]-applicationLetterDestinationY)*0.06 ;
    }
    text(applicationStr[j],applicationLetterPosX[j],applicationLetterPosY[j]);
  }




  //logo
  logoPixelInPosCount = 0;
  for(var j = 0; j<logoCounter; j++){
    //color
    switch (logoPixelColor[j]) {
      case "1":
        fill(color('black'));
        break;
      case "6":
        fill(color('grey'));
        break;
      case "7":
        fill(color('white'));
        break;
      case "9":
        fill(color('DarkGray'));
        break;
      default:
        fill(color(0,0,0));
        break;

    }


    //pixelRectangle
    rect(logoPixelPosX[j]-logoPixelSize*0.5,logoPixelPosY[j]-logoPixelSize*0.5,logoPixelSize*logoPixelDist,logoPixelSize*logoPixelDist);

    //pixelMovement
    if(logoUpdatePixels){
      if(abs(logoPixelPosX[j]-logoPixelDestinationX[j]) <= 2)
      {
        logoPixelPosX[j] = logoPixelDestinationX[j];
      }
      else
      {
        var velX;
        if(logoPixelPosX[j]<logoPixelDestinationX[j]){
          velX = 2;
        }
        else {
          velX = -2;
        }
        logoPixelPosX[j] += velX*abs(logoPixelPosX[j]-logoPixelDestinationX[j])*0.125 * random(0.125,0.25);
      }
      if(abs(logoPixelPosY[j]-logoPixelDestinationY[j]) <= 2)
      {
        logoPixelPosY[j] = logoPixelDestinationY[j];
      }
      else
      {
        var velY;
        if(logoPixelPosY[j]<logoPixelDestinationY[j]){
          velY = 2;
        }
        else {
          velY = -2;
        }
        logoPixelPosY[j] += velY*abs(logoPixelPosY[j]-logoPixelDestinationY[j])*0.125 * random(0.125,0.25);
      }
      if(abs(logoPixelPosX[j]-logoPixelDestinationX[j]) <= 2 && abs(logoPixelPosY[j]-logoPixelDestinationY[j]) <= 2)
      {
        logoPixelInPosCount++;
      }
    }
    else{
      noLoop();
    }
  }
  if(logoPixelInPosCount >= logoCounter){
    logoUpdatePixels = false;
  }
}

function windowResized() {
  resizeCanvas(windowWidth-windowWidth*0.005,windowHeight-windowWidth*0.005);
  //logo
  for(var j=0; j<logoCounter; j++){
    var oldPosX = logoPixelDestinationX[j];
    var oldPosY = logoPixelDestinationY[j];
    oldPosX = oldPosX / logoPixelSize;
    oldPosY = oldPosY / logoPixelSize;
    logoPixelDestinationX[j] = oldPosX * (windowWidth/logoScaleDivider);
    logoPixelDestinationY[j] = oldPosY * (windowWidth/logoScaleDivider);
  }


  //application string
  applicationLetterSize = windowWidth/logoScaleDivider*8;
  var oldPosY = applicationLetterDestinationY;
  oldPosY = oldPosY / logoPixelSize;
  applicationLetterDestinationY = oldPosY * (windowWidth/logoScaleDivider);
  for(var j=0; j<applicationStr.length; j++)
  {
    var oldPosX = applicationLetterDestinationX[j];
    oldPosX = oldPosX / logoPixelSize;
    applicationLetterDestinationX[j] = oldPosX * (windowWidth/logoScaleDivider);
  }

  logoPixelSize = windowWidth/logoScaleDivider;
  logoUpdatePixels = true;
  loop();
}

function mouseWheel(event) {

  top = document.body.scrollTop;
  left = document.body.scrollLeft;
  scrollPosY -= event.delta*0.01;
  if(scrollPosY>=-3){
    //logo
    for(var j=0; j<logoCounter; j++){
      logoPixelDestinationY[j]-= event.delta;
    }
    logoUpdatePixels = true;
    //application str
    applicationLetterDestinationY -= event.delta;
  }

  loop();

  //block page scrolling
  //return false;
}
