var applicationStr = ['A','p','p','l','i','c','a','t','i','o','n',' ','@'];
var applicationLetterPosX = [];
var applicationLetterPosY = [];
var applicationLetterDestinationX = [];
var applicationLetterDestinationY;
var applicationLetterSize = 48;
var applicationLetterInPosCount = 0;
